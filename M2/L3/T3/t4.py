'''
Програма повинна запитати "Скільки буде 5 + 6?". 
Користувач вводить відповідь і якщо вона вірна, 
програма друкує "Правильно!". У разі неправильної 
відповіді друкує "Ні! Спробуй ще раз!" і знову пропонує ввести відповідь.
'''


correct_answer = 5 + 6

while True:
    user_answer = int(input("Скільки буде 5 + 6? "))

    if user_answer == correct_answer:
        print("Правильно!")
        break
    else:
        print("Ні! Спробуй ще раз.")
